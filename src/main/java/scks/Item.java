package scks;

/**
 *
 * @author jegav
 */
public class Item {
    private String id;
    private String usuario;

    public Item(String id, String usuario) {
        this.id = id;
        this.usuario = usuario;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    @Override
    public String toString() {
        return usuario;
    }
    
    
    
}
