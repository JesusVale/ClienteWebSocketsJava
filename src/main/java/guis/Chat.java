    @Override
    public void nuevoCliente(List<Map<String, String>> clientes) {
        this.userCbx.removeAll();
        List<Object> usuarios = new ArrayList();
        usuarios.add("TODOS");
        for(Map<String, String> cliente: clientes){
            usuarios.add(new Item(cliente.get("id"), cliente.get("usuario")));
        }
        this.userCbx.setModel(new DefaultComboBoxModel(usuarios.toArray()));
    }

    @Override
    public void nuevoMensaje(String mensaje) {
        this.chatTxt.append(mensaje+"\n");
    }

    @Override
    public void nuevoContacto(Contacto contacto) {
        
        this.tablaContacto.setValueAt(contacto.getNombre(), 0, 0);
        this.tablaContacto.setValueAt(contacto.getEmail(), 0, 1);
        this.tablaContacto.setValueAt(contacto.getEdad(), 0, 2);
    }
