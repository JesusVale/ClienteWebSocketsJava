    public String getAlcance(){
        String alcance = "TODOS";
        if(this.userCbx.getSelectedIndex() > 0){
            Item seleccionado = (Item) this.userCbx.getSelectedItem();
            alcance = seleccionado.getId();
        }
        return alcance;
    }
    

